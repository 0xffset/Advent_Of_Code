static bool canHold(const std::vector<std::array<int, 3>> &cur, int a, int c) {
    for (const auto &arr : cur) {
        if (arr == std::array<int, 3>{-1, 0, 0}) {
            return false;
        } else if (arr.at(1) == a && arr.at(2) == c) {
            return true;
        } else if (in.find(std::array<int, 2>{arr.at(1), arr.at(2)}) != in.end()) {
            if (canHold(in.at(std::array<int, 2>{arr.at(1), arr.at(2)}), a, c)) {
                return true;
            }
        }
    }
    return false;
}

static long bagCount(const std::vector<std::array<int, 3>> &cur, int a, int c) {
    long out = 0l;
    for (const auto &arr : cur) {
        if (arr == std::array<int, 3>{-1, 0, 0}) {
            return 0l;
        } else if (in.find(std::array<int, 2>{arr.at(1), arr.at(2)}) != in.end()) {
            out += arr.at(0) * bagCount(in.at(std::array<int, 2>{arr.at(1), arr.at(2)}), a, c);
            out += arr.at(0);
        } else {
            out += arr.at(0);
        }
    }
    return out;
}

